# FOR LOOP
def sum_num():
    for i in range(0, 101, 2):
        print(i)


sum_num()


# WHILE LOOP
def sum_num():
    x = 0
    while x <= 101:
        print(x)
        x += 2


sum_num()


def sum_num():
    num = int(input("Enter a integer: "))
    sum = 0

    for i in range(1, num + 1):
        sum += i
    print(sum)









